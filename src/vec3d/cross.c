#include "vec3d.h"

inline t_vec3d	vec3d_cross(const t_vec3d a, const t_vec3d b)
{
	return ((t_vec3d){
		a.y * b.z - a.z * b.y,
		a.z * b.x - a.x * b.z,
		a.x * b.y - a.y * b.x
	});
}
